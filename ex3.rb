# + (plus)
# - (minus)
# / (slash)
# * (asterisk)
# % (percent)
# < (less-than)
# > (greater-than)
# <= (less-than-equal)
# >= (greater-than-equal)

# This line prints the string.
puts "I will now count my chickens:"

# This line prints the string and interpolates the equation for hens.
puts "Hens #{25.0 + 30.0 / 6.0}"
# This line prints the string and interpolates the equation for roosters
puts "Roosters #{100.0 - 25.0 * 3.0 % 4.0}"

#This line prints the string.
puts "Now I will count the eggs:"

#This line prints the result of the equation.
puts 3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0

#This line prints the string.
puts "Is it true that 3 + 2 < 5 - 7?"

#This line prints the result of the equation.
puts 3.0 + 2.0 < 5.0 - 7.0

#This line print the string and interpolates the equation.
puts "What is 3 + 2? #{3.0 + 2.0}"
#This line print the string and interpolates the equation.
puts "What is 5 - 7? #{5.0 - 7.0}"

#This line print the string.
puts "Oh, that's why it's false."

#This line print the string.
puts "How about some more."

#This line print the string and interpolates the equation.
puts "Is it greater? #{5.0 > -2.0}"
#This line print the string and interpolates the equation.
puts "Is it greater or equal? #{5.0 >= -2.0}"
#This line print the string and interpolates the equation.
puts "Is it less or equal? #{5.0 <= -2.0}"
